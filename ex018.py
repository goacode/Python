from math import sin, cos, tan, radians
angulo = float(input("Insira o angulo desejado: "))

print(f"{angulo}° possui um SENO de {sin(radians(angulo)):.2f}")
print(f"{angulo}° possui um COSSENO de {cos(radians(angulo)):.2f}")
print(f"{angulo}° possui uma TANGENTE de {tan(radians(angulo)):.2f}")
